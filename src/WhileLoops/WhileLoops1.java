package WhileLoops;

public class WhileLoops1 {

    public static void main(String[] args) {

        /*
        While loop kullanarak 2 basamakli 7 ile bolunebilen pozitif tamsayilari
        yazdirin
         */

        int sayi = 14;

        while (sayi < 100) {
            System.out.println(sayi);
            sayi += 7;
        }
    }


}
