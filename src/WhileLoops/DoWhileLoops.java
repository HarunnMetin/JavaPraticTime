package WhileLoops;

public class DoWhileLoops {
    public static void main(String[] args) {

        /*
        while dongusu kullanarak 1'den 10'a kadar olan sayilari ekrana yazdiracak bir java
        programi yaziniz.
         */

        int sayi = 1;

        while (sayi<=10){
            System.out.println(sayi);
            sayi++;
        }


    }
}
